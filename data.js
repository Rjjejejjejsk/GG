function getAllPhrases() {
    let all = [];
    for (const [author, data] of Object.entries(phrasesDatabase.authors)) {
        data.phrases.forEach(p => {
            all.push({
                text: p.text,
                author,
                category: p.category,
                keywords: p.keywords,
                type: data.type,
                source: p.source
            });
        });
    }
    return all;
}
