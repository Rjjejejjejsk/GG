// Отримуємо всі фрази у єдиному масиві
function getAllPhrases() {
    const all = [];
    for (const [author, data] of Object.entries(phrasesDatabase.authors)) {
        for (const p of data.phrases) {
            all.push({
                text: p.text,
                author,
                category: p.category,
                keywords: p.keywords,
                source: p.source,
                type: data.type
            });
        }
    }
    return all;
}

