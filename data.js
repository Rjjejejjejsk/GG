// Функція для отримання всіх фраз у зручному форматі
function getAllPhrases() {
    let allPhrases = [];
    for (const [author, data] of Object.entries(phrasesDatabase.authors)) {
        data.phrases.forEach(phrase => {
            allPhrases.push({
                text: phrase.text,
                author: author,
                category: phrase.category,
                keywords: phrase.keywords,
                type: data.type
            });
        });
    }
    return allPhrases;
}

